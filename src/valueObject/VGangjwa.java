package valueObject;

public class VGangjwa {

	private String num;
	private String title;
	private String pro;
	private String time;
	
	public VGangjwa(String num, String title, String pro, String time) {
		this.num = num;
		this.title = title;
		this.pro = pro;
		this.time = time;
	}
	
	public String getNum() {
		return num;
	}

	public String getTitle() {
		return title;
	}

	public String getPro() {
		return pro;
	}

	public String getTime() {
		return time;
	}


	


}
