package valueObject;

public class VGangjwa {

	private String num;
	private String name;
	private String pro;
	private String time;
	
	public VGangjwa(String num, String name, String pro, String time) {
		this.num = num;
		this.name = name;
		this.pro = pro;
		this.time = time;
	}
	
	public String getNum() {
		return num;
	}

	public String getName() {
		return name;
	}

	public String getPro() {
		return pro;
	}

	public String getTime() {
		return time;
	}


	


}
